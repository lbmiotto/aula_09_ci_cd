def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(n1, n2):
    som = n1 + n2
    return som

def subtracao(n1, n2):
    sub = n1 - n2
    return sub

def divisao(n1,n2):
    divs = n1/n2
    return divs

def multiplicacao(n1,n2):
    mult = n1*n2
    return mult